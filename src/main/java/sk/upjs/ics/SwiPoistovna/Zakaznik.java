public class Zakaznik {
    private int vek;
    private int rizikovaSkupina;
    private boolean zamestnany;
    private double prijem;

    public int getVek() {
        return vek;
    }

    public void setVek(int vek) {
        this.vek = vek;
    }

    public int getRizikovaSkupina() {
        return rizikovaSkupina;
    }

    public void setRizikovaSkupina(int rizikovaSkupina) {
        this.rizikovaSkupina = rizikovaSkupina;
    }

    public boolean isZamestnany() {
        return zamestnany;
    }

    public void setZamestnany(boolean zamestnany) {
        this.zamestnany = zamestnany;
    }

    public double getPrijem() {
        return prijem;
    }

    public void setPrijem(double prijem) {
        this.prijem = prijem;
    }
    
    
}
